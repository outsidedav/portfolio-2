            <h3>Secure your Passwords</h3>
            <p>I just began working as a UX designer with a local agency named Colours. Colours is a boutique design and dev shop with no more than a couple employees that works with local startups to design user stories, flow, and experience. This has led to the opportunity of working with several companies that have hired us to design, develop and test their mobile apps.</p>
            <p>Most recently I've been working on an app called Junify. Junify allows companies to protect their account passwords, by giving access to their employees without the need to give them the password. Junify improves security for the employer, while simplifying the log in proses for the employee. </p>
            <h4>Creating the User Flow and Story</h4>
            <p>Since Junify has already engineered the app, though without graphics, the first step was to look at their IA and decide how well the user would interact with the app. We interviewed a few potential users and asked simple questions like: "At any point were you confused?" and "Were you able to preform the task you expected?"</p>
            <p>The issues with the app as engineered were immediately apparent. We found 3 main issues confused users. </p>
            <p>‍</p>
            <ol>
                <li>There was no clear way to sign out</li>
                <li>The setup process was not clear enough, users would often go to the wrong page to add a service like Slack or Facebook.</li>
                <li>Users consistently mixed up a Card with a group.</li>
            </ol>
            <figure class="w-richtext-align-center" style="max-width: 60%;" data-rt-type="image" data-rt-align="center" data-rt-max-width="">
                <div><img src="https://daks2k3a4ib2z.cloudfront.net/592103aa16b99b0139ddc5f9/592597ce6e5daf2bfe682d0f_userflow.png"></div>
            </figure>
            <p>We solved these issues by adding and moving a few of the features. </p>
            <ol>
                <li>We added a sign out flow to the menu</li>
                <li>Added signs and simples that guided the user through the proper set up flow.</li>
                <li>We created parameters that made is so the user could only add a group, and then a card, removing the confusion of when to add which.</li>
            </ol>
